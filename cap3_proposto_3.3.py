A = float(input('Digite um valor real para A: '))
B = float(input('Digite um valor real para B: '))
C = float(input('Digite um valor real para C: '))

R1 = A + B + C
print(f'R1 = {R1:.3f}')
R2 = A * B * C
print(f'R2 = {R2:.3f}')
R3 = 2 * (A + B) - C
print(f'R3 = {R3:.3f}')
R4 = (A + B + C) / 3
print(f'R4 = {R4:.3f}')
R5 = (2 * B + 3 * C) / (5 * A)
print(f'R5 = {R5:.3f}')
R6 = A ** 2 + B ** 2
print(f'R6 = {R6:.3f}')

